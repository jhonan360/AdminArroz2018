    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        char[] Pass = txtContrasena.getPassword();
       String contrasena = new String(Pass);
        char[] Pass1 = txtConfiContraseña.getPassword();
       String confiContrasena = new String(Pass1);
        if (confiContrasena.equals(contrasena)) {
                    if (!vali.ValidarContrasena(contrasena)) {
                        JOptionPane.showMessageDialog(null, "La contraseña debe tener números y letras con minimo 7 caracteres");
                    } else {
                        try {
                            String contraencript = encrip.encrypt(contrasena);
                            try {
                                Con = new Conexion();
                                st = Con.conexion.createStatement();
                                st.executeUpdate("UPDATE usuario SET contrasena='" + contraencript + "', cambioContrasena='false' WHERE usuario.user='" + login.enviarUsuario() + "'");
                                ext.logs("UPDATE", "UPDATE usuario SET contrasena='" + contraencript + "', cambioContrasena='false' WHERE usuario.user='" + login.enviarUsuario() + "'");
                                Con.Desconectar();
                                JOptionPane.showMessageDialog(null, "Contraseña cambiada exitosamente");
                                dispose();
                            } catch (Exception e) {
                                e.printStackTrace();
                            }
                        } catch (Exception e) {
                            e.printStackTrace();
                        }
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "La contraseña no coincide");
                }
    }//GEN-LAST:event_btnGuardarActionPerformed

